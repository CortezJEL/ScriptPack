local IsPlaying = false
local Pitch = 1
local Volume = 1
local Looped = false
local Sound = Instance.new("Sound", script)
Sound.Name = "SoundList"
Sound.Pitch = Pitch
Sound.Volume = Volume
Sound.Looped = Looped


game:GetService("Players").DarkShadow6.Chatted:connect(function(Msg)
	Msg = Msg:lower()
	if Msg == "load" then
		if IsPlaying == true then
			Sound:Stop()
			IsPlaying = false
			wait(1)
		end
		Sound.Pitch = 1
		Sound.Volume = 0
		Sound.Looped = false
		local Hint = Instance.new("Hint", script)
		local Text = ""
		coroutine.wrap(function()
			local Mod = 0
			while Hint.Parent ~= nil and Text ~= nil do
				Mod = Mod + 1
				if Mod > 4 then
					Mod = 1
				end
				Hint.Text = Text.. " " ..(function()
					if Mod == 1 then
						return "|"
					elseif Mod == 2 then
						return "/"
					elseif Mod == 3 then
						return "-"
					elseif Mod == 4 then
						return "\\"
					end
				end)()
				wait(0.1)
			end
		end)()
		for i = 1, #SoundList, 2 do
			Text = math.floor((i / #SoundList) * 100).. "%"
			Sound.SoundId = SoundList[i + 1]
			wait()
			Sound:Play()
			wait()
			Sound:Stop()
			wait()
		end
		Text = nil
		Hint.Text = "All sounds loaded!"
		wait(1)
		Sound.Pitch = Pitch
		Sound.Volume = Volume
		Sound.Looped = Looped
		Hint:Remove()
	elseif Msg:sub(0, 5) == "play " then
		if IsPlaying == true then
			Sound:Stop()
			IsPlaying = false
			wait(1)
		end
		for i = 1, #SoundList, 2 do
			if SoundList[i]:lower():match(Msg:sub(6)) then
				local Hint = Instance.new("Hint", script)
				Hint.Text = "Playing \"" ..SoundList[i].. "\"..."
				game:GetService("Debris"):AddItem(Hint, 5)
				Sound.SoundId = SoundList[i + 1]
				Sound:Play()
				IsPlaying = true
				break
			end
		end
	elseif Msg:sub(0, 7) == "playid " then
		if IsPlaying == true then
			Sound:Stop()
			IsPlaying = false
			wait(1)
		end
		Sound:Stop()
		wait(1)
		local Hint = Instance.new("Hint", script)
		Hint.Text = "Playing Asset ID \"" ..Msg:sub(8).. "\"..."
		game:GetService("Debris"):AddItem(Hint, 5)
		Sound.SoundId = "http://www.roblox.com/Asset/?id=" ..Msg:sub(8)
		Sound:Play()
		IsPlaying = true
	elseif Msg:sub(0, 8) == "playurl " then
		if IsPlaying == true then
			Sound:Stop()
			IsPlaying = false
			wait(1)
		end
		Sound:Stop()
		wait(1)
		local Hint = Instance.new("Hint", script)
		Hint.Text = "Playing URL \"" ..Msg:sub(9).. "\"..."
		game:GetService("Debris"):AddItem(Hint, 5)
		Sound.SoundId = Msg:sub(9)
		Sound:Play()
		IsPlaying = true
	elseif Msg == "pause" then
		Sound:Pause()
		IsPlaying = false
	elseif Msg == "stop" then
		Sound:Stop()
		IsPlaying = false
	elseif Msg:sub(0, 6) == "pitch " then
		Sound.Pitch = tonumber(Msg:sub(7)) or 1
		Pitch = Sound.Pitch
	elseif Msg:sub(0, 7) == "volume " then
		Sound.Volume = tonumber(Msg:sub(8)) or 1
		Volume = Sound.Volume
	elseif Msg == "loop" then
		Sound.Looped = not Sound.Looped
		Looped = Sound.Looped
	end
end)